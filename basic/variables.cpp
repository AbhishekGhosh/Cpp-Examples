#include <iostream>

int main() {
    // Integer variable
    int num = 42;

    // Floating-point variable
    float pi = 3.14;

    // Character variable
    char letter = 'A';

    // Output variables
    std::cout << "Integer: " << num << std::endl;
    std::cout << "Float: " << pi << std::endl;
    std::cout << "Character: " << letter << std::endl;

    return 0;
}
