#include <iostream>

int main() {
    int num = 10;

    // If-else statement
    if (num > 0) {
        std::cout << "Positive" << std::endl;
    } else {
        std::cout << "Non-positive" << std::endl;
    }

    // For loop
    for (int i = 0; i < 5; ++i) {
        std::cout << "Iteration " << i + 1 << std::endl;
    }

    return 0;
}
